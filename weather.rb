fav_weather = 'rainbow'

case fav_weather
when 'sunny'
	puts "Yay, the sun's shining!"
when 'cloudy'
	puts "Meh, it's grey and dull."
when 'rainy'
	puts "It's raining men, hallelujah it's raining men!"
when 'snowy'
	puts "Dashin through the snow..."
when 'hailing'
	puts "It's raining golf balls"
when 'stormy'
	puts "Careful not to fly away"
when 'thunderstorm'
	puts "Look and them pretty lightnings!"
else 
	puts "OK, I can't help...RUN!"
end