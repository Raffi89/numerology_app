fav_color = 'pink'

case fav_color
when 'red'
	puts "Red like fire!"
when 'orange'
	puts "Orange like, an orange..."
when 'yellow'
	puts "Yellow daffodils are so pretty in spring!"
when 'green'
	puts "Green like hope (Verde speranza)"
when 'blue'
	puts "Blue like the sky or the ocean!"
when 'purple'
	puts "Blue and yellow purple hills!"
else
	puts "Hmm, I don't know if I have one..."
end